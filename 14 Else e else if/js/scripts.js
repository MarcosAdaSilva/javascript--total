var nome = "Kledea";
// O operador Else é o oposto do if e é executado sempre depois

if (nome == "João") {
  console.log("O nome dele é João");
} else if (nome == "Marcos") {
  console.log("O nome é Marcos");
} else if (nome == "Kledea") {
  console.log("O nome é Kledea");
} else {
  console.log("Ele possue outro nome!");
}

